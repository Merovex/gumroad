    def process(dry_run: false, additional_ids: [])
      ids = (PURCHASE_IDS + additional_ids.map(&:to_i)).uniq
      stats = Hash.new(0)

      Purchase.where(id: ids).find_in_batches(batch_size: BATCH_SIZE) do |batch|
        ReplicaLagWatcher.watch
        batch.each do |purchase|
          stats[:scanned] += 1

          unless eligible?(purchase)
            stats[:skipped_state_drifted] += 1
            puts "skip purchase #{purchase.id} (state drifted)"
            next
          end

          if dry_run
            stats[:would_restore] += 1
            puts "DRY-RUN would restore purchase #{purchase.id} (bundle=#{purchase.is_bundle_purchase?})"
            next
          end

          ActiveRecord::Base.transaction do
            purchase.update!(chargeback_reversed: true)
            purchase.mark_giftee_purchase_as_chargeback_reversed if purchase.is_gift_sender_purchase
            purchase.mark_product_purchases_as_chargeback_reversed!
          end
          stats[:restored] += 1
          puts "restored purchase #{purchase.id}"
        end
      end

      stats[:not_found_in_db] = ids.size - stats[:scanned]
      puts "done: #{stats.to_h}"
      stats.to_h
    end

    private
      def eligible?(purchase)
        purchase.charge_processor_id == PaypalChargeProcessor.charge_processor_id &&
          purchase.purchase_state == "successful" &&
          purchase.chargeback_date.present? &&
          !purchase.chargeback_reversed? &&
          !purchase.stripe_refunded &&
          purchase.stripe_partially_refunded
      end
